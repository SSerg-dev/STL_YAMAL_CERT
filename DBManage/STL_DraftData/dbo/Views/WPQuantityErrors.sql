CREATE VIEW dbo.WPQuantityErrors
AS
SELECT        ER.Log_ID, ER.Num, ER.Title, ER.Unit, ER.Marka, ER.Activity_Desc, ER.UOM, ER.Design_Target, ER.Fact_Volume, ER.Fact_Percent, ER.Test_Done, ER.Acts_Prepared_CNT, ER.Acts_Prepared_Percent, ER.Reg, 
                         ER.Under_Review_CNT, ER.Under_Review_Percent, ER.Commented_CNT, ER.Commented_Percent, ER.Approved_CNT, ER.Approved_Percent, ER.Multiple, ER.Issues, ER.Source_File, ER.Load_Date, ER.Error_Code, 
                         ER.Error_Column, CASE WHEN Len(ER.Reason) > 3 THEN ER.Reason ELSE E.Description_Rus + ' / ' + E.Description_Eng END AS Reason
FROM            dbo.s_WPQuantityErrors AS ER LEFT OUTER JOIN
                         dbo.p_ValidationErrors AS E ON E.Number = ER.Reason

