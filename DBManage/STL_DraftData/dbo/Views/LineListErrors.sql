AS
SELECT        E.Isometric_No, E.DesignArea, E.Line_nb, E.Number_Line_List, E.Russian_Document_Reference_LL, E.Title_According_LL, E.Marka_According_LL, E.PID, E.Source_File, E.Load_Date, E.Error_Code, E.Error_Column, 
                         CASE WHEN Len(E.Reason) > 3 THEN E.Reason ELSE d .Description_Rus + ' / ' + d .Description_Eng END AS Reason_TXT, E.Process_Phase
FROM            dbo.s_LineListErrors AS E LEFT OUTER JOIN
                         dbo.p_ValidationErrors AS d ON E.Reason = CAST(d.Number AS nvarchar(10))

