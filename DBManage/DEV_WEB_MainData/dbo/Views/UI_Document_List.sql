CREATE VIEW dbo.UI_Document_List
AS
SELECT        d .Document_ID, d .Document_Code, d .Document_Number, d .DocumentType_ID, d .Document_Date, d .Issue_Date, d .TotalSheets, d .Document_Name, d .Parent_ID, d .VersionNumber, d .IsActual, dt.DocumentType_Code, 
                         s.Status_ID, s.Description_Rus AS Status_Name, dts.Document_to_Status_ID, sum(iif(dtf.Document_to_FileTable_ID IS NULL, 0, 1)) AS FilesQTY
FROM            dbo.Document AS d LEFT OUTER JOIN
                         dbo.DocumentType AS dt ON d .DocumentType_ID = dt.DocumentType_ID LEFT JOIN
                         dbo.Document_to_FileTable dtf ON d .Document_ID = dtf.Document_ID JOIN
                         dbo.Document_to_Status dts ON d .Document_ID = dts.Document_ID JOIN
                         dbo.Status s ON dts.Status_ID = s.Status_ID
GROUP BY d .Document_ID, d .Document_Code, d .Document_Number, d .DocumentType_ID, d .Document_Date, d .Issue_Date, d .TotalSheets, d .Document_Name, d .Parent_ID, d .VersionNumber, d .IsActual, dt.DocumentType_Code, 
                         s.Status_ID, s.Description_Rus, dts.Document_to_Status_ID

GO
